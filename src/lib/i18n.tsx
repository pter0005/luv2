type Locale = keyof typeof translations;
type TranslationKey = keyof typeof translations.pt;

interface LanguageContextType {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [locale, setLocale] = useState<Locale>('pt');
  const [isInitialized, setIsInitialized] = useState(false);

  useEffect(() => {
    if (typeof window !== "undefined") {
      const hostname = window.location.hostname;

      if (hostname.endsWith('.com.br')) {
        setLocale('pt');
      } else { // For .net, localhost, etc.
        const browserLang = navigator.language.split('-')[0] as Locale;
        if (browserLang === 'es') {
          setLocale('es');
        } else {
          setLocale('en'); 
        }
      }
      setIsInitialized(true);
    }
  }, []);

  const t = useCallback((key: TranslationKey, vars?: Record<string, string | number>) => {
    let translation = translations[locale]?.[key] || translations['pt'][key] || key;
    if (vars) {
      Object.keys(vars).forEach(varKey => {
        const regex = new RegExp(`\\{${varKey}\\}`, 'g');
        translation = translation.replace(regex, String(vars[varKey]));
      });
    }
    return translation;
  }, [locale]);
  
  const value = useMemo(() => ({ locale, setLocale, t }), [locale, t]);

  if (!isInitialized) {
    return null; // Render nothing until the locale is determined
  }

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }
  return context;
};
