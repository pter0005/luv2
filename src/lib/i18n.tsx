type Locale = 'pt' | 'en' | 'es';
type TranslationKey = keyof (typeof translations.pt & typeof translations.en & typeof translations.es);

interface LanguageContextType {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider = ({ children, initialLocale = 'pt' }: { children: ReactNode; initialLocale?: Locale }) => {
  const [locale, setLocale] = useState<Locale>(initialLocale);

  const t = useCallback((key: TranslationKey, vars?: Record<string, string | number>) => {
    let translation = translations[locale]?.[key] || translations['en']?.[key] || key;
    if (vars) {
      Object.keys(vars).forEach(varKey => {
        const regex = new RegExp(`\\{${varKey}\\}`, 'g');
        translation = translation.replace(regex, String(vars[varKey]));
      });
    }
    return translation;
  }, [locale]);
  
  const value = useMemo(() => ({ locale, setLocale, t }), [locale, t]);

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }
  return context;
};


    